// Reference data for numbers, counters, and time expressions in
// Minna no Nihongo. Each "card" shows a 1-10 reading table; entries
// flagged `special: true` are sound changes or irregular readings —
// those get visual emphasis in the UI.

// Helper to build a straightforward 1-10 table (no sound changes).
function regular(suffix, romajiSuffix) {
  const ones = [
    ["いち", "ichi"],
    ["に", "ni"],
    ["さん", "san"],
    ["よん", "yon"],
    ["ご", "go"],
    ["ろく", "roku"],
    ["なな", "nana"],
    ["はち", "hachi"],
    ["きゅう", "kyū"],
    ["じゅう", "jū"],
  ];
  return ones.map(([k, r], i) => ({
    n: i + 1,
    kana: k + suffix,
    romaji: r + romajiSuffix,
  }));
}

